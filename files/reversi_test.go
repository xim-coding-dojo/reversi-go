package main

import (
	"github.com/stretchr/testify/assert"
	"testing"
)

func Test_mark_player_moves(t *testing.T) {
	var board = [8][8]byte{
		{'.', '.', '.', '.', '.', '.', '.', '.'},
		{'.', '.', '.', '.', '.', '.', '.', '.'},
		{'.', '.', '.', '.', '.', '.', '.', '.'},
		{'.', '.', '.', 'B', 'W', '.', '.', '.'},
		{'.', '.', '.', 'W', 'B', '.', '.', '.'},
		{'.', '.', '.', '.', '.', '.', '.', '.'},
		{'.', '.', '.', '.', '.', '.', '.', '.'},
		{'.', '.', '.', '.', '.', '.', '.', '.'},
	}

	var result_board = [8][8]byte{
		{'.', '.', '.', '.', '.', '.', '.', '.'},
		{'.', '.', '.', '.', '.', '.', '.', '.'},
		{'.', '.', '.', '.', '0', '.', '.', '.'},
		{'.', '.', '.', 'B', 'W', '0', '.', '.'},
		{'.', '.', '0', 'W', 'B', '.', '.', '.'},
		{'.', '.', '.', '0', '.', '.', '.', '.'},
		{'.', '.', '.', '.', '.', '.', '.', '.'},
		{'.', '.', '.', '.', '.', '.', '.', '.'},
	}

	assert.Equal(t, result_board, markPlayersNextMoves(&board, 'B'))
}
